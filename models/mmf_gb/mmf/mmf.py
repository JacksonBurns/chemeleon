_SMARTS_QS = [Chem.MolFromSmarts(s) for s in MMF_SMARTS]

def morgan_mined_fingerprint(mol: Chem.Mol) -> list[int]:
    """
    Convert an RDKit molecule into a binary Morgan Minded Fingerprint.

    Parameters
    ----------
    mol : Chem.Mol
        The molecule to fingerprint.

    Returns
    -------
    List[int]
        A list of 0/1 values, one per substructure.
    """
    fp = []
    for qs in _SMARTS_QS:
        fp.append(int(mol.HasSubstructMatch(qs)))
    return fp

